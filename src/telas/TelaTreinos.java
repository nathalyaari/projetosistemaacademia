    private void carregarTabela(List<Treino> lista) {
        DefaultTableModel modelo = (DefaultTableModel) tabelaTreinos.getModel();
        modelo.setRowCount(0); // limpa a tabela

        for (Treino h : lista) {
            modelo.addRow(new Object[]{
                h.getTipo(),
                h.getDataInicio(),
                h.getDuracao()
            });
        }
    }

