    private void campoBuscaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_campoBuscaActionPerformed
        // TODO add your handling code here:
        String termo = campoBusca.getText().trim();
        if (termo.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Digite o nome ou ID do membro.");
            return;
        }
        
        TreinoDAO dao = new TreinoDAO();
        listaAtual = dao.buscarPorMembro(termo);

        if (listaAtual.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Nenhum treino encontrado.");
            return;
        }

        idMembroAtual = listaAtual.get(0).getIdMembro(); 
        carregarTabela(listaAtual);
   
        
    }//GEN-LAST:event_campoBuscaActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // TODO add your handling code here:
        String termo = campoBusca.getText().trim();

    if (termo.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Digite o nome ou ID do membro.");
        return;
    }

    TreinoDAO dao = new TreinoDAO();
    List<Treino> listaAtual = dao.buscarPorMembro(termo);

    if (listaAtual.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Nenhum treino encontrado.");
        return;
    }

    
    idMembroAtual = listaAtual.get(0).getIdMembro();

    
    carregarTabela(listaAtual);

    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        // TODO add your handling code here:
         int linha = tabelaTreinos.getSelectedRow();
if (linha == -1) {
    JOptionPane.showMessageDialog(this, "Selecione uma atividade para excluir.");
    return;
}

int confirmacao = JOptionPane.showConfirmDialog(this, "Tem certeza que deseja excluir este histórico?", "Confirmação", JOptionPane.YES_NO_OPTION);
if (confirmacao != JOptionPane.YES_OPTION) {
    return;
}

int id = listaAtual.get(linha).getId();
idMembroAtual = listaAtual.get(0).getIdMembro();

new TreinoDAO().excluir(id);

List<Treino> listaAtualizada = new TreinoDAO().buscarPorMembro(String.valueOf(idMembroAtual));
carregarTabela(listaAtualizada);

listaAtual = new TreinoDAO().buscarPorMembro(String.valueOf(idMembroAtual));
carregarTabela(listaAtual);
    }//GEN-LAST:event_btnExcluirActionPerformed

