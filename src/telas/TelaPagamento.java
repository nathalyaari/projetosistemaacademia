    private void botaoConfirmarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoConfirmarActionPerformed
        // TODO add your handling code here:
try {
    int linha = tabelaPagamentos.getSelectedRow();
    if (linha == -1) {
        JOptionPane.showMessageDialog(this, "Nenhum pagamento selecionado.");
        return;
    }

    Pagamento selecionado = listaAtual.get(linha);
    int idPagamento = selecionado.getId();

    String tipo = comboPagamento.getSelectedItem().toString();
    String dataHoje = java.time.LocalDate.now().toString(); 

    PagamentoDAO dao = new PagamentoDAO();
    dao.atualizarStatusParaPago(idPagamento, dataHoje);

    selecionado.setStatus("PAGO");
    selecionado.setDataPagamento(dataHoje);

  
    tabelaPagamentos.setValueAt("PAGO", linha, 2);
    tabelaPagamentos.setValueAt(dataHoje, linha, 1);

    JOptionPane.showMessageDialog(null, "Pagamento atualizado como PAGO via " + tipo + "!");
    dispose();

} catch (Exception e) {
    e.printStackTrace();
    JOptionPane.showMessageDialog(null, "Erro ao atualizar pagamento: " + e.getMessage());
}



    }//GEN-LAST:event_botaoConfirmarActionPerformed

